// ============================================================
//  PREDICT BEHAVIOR
// ============================================================
function predictBehavior(attractorId, situationId, age) {
  const strats = STRATEGIES[situationId];
  if (!strats) return [];

  const adjusted = strats.map(s => ({
    name: s.name,
    raw: Math.max(0.01, s.baseProb + s.ageModifier(age))
  }));

  const total = adjusted.reduce((sum, s) => sum + s.raw, 0);
  const result = adjusted.map(s => ({
    name: s.name,
    probability: Math.round((s.raw / total) * 100)
  }));

  // Fix rounding to sum to 100
  const diff = 100 - result.reduce((s, r) => s + r.probability, 0);
  if (diff !== 0) result[0].probability += diff;

  return result.sort((a, b) => b.probability - a.probability);
}
